#Se o valor lido for maior que 10, caso contrário escrever:
#Não é maior que 10!
#se o valor for igual a 10 entao escreva "O numero é igual a 10"
valor = int(input("Digite um valor: "))

if valor < 10:
    print ("É Menor que 10 ")

elif valor == 10:
    print ("É igual a 10")

else:
    print ("É Maior que 10!")

